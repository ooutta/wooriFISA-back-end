// java파일은 항상 대문자로 시작
// 하나의 java 파일은 최소 하나의 클래스를 가지고 있다.
// Hello.java 파일은 하나의 class인 Hello를 가지고 있다.

public class Hello {
    
    // Hello class가 가진 main() method
    public static void main(String[] args) {
        System.out.println("Hello Java");
    } // end main
} // end Hello

